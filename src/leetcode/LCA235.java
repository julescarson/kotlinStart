package leetcode;

public class LCA235 {
    public InverBinaryTree226.TreeNode lowestCommonAncestor(InverBinaryTree226.TreeNode root, TreeNode p, TreeNode q) {
        while (root != null) {
            if (p.val > root.val && q.val > root.val) {
                root = root.right;
            } else if (p.val < root.val && q.val < root.val) {
                root = root.left;
            } else {
                return root;
            }
        }
        return null;
    }
}
